char	*ft_strchr(const char *s, int c)
{
	char	*pt;

	pt = (char *) s;
	while (*pt != 0)
	{
		if (*pt == c)
			return (pt);
		pt++;
	}
	if (*pt == c)
		return (pt);
	return (0);
}
